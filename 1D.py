import numpy as np

n = np.array([1, 2, 3, 4, 5])

print(n[0])  # Accessing the first element
print(n[0:3])  # Slicing the first three elements
print(n[1:])  # Slicing from the second element to the end
print(n[-1])  # Accessing the last element
print(n[-3:])  # Slicing the last three elements
print(n[-3:-1])  # Slicing from the third last to the second last
